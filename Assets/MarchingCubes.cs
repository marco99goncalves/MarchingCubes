    void InitializePoints()
    {
        numberOfPoints = (int)(size/res);
        points = new int[numberOfPoints, numberOfPoints, numberOfPoints];
        createdTriangles = new List<GameObject>();
        if (showPoints){
            for (int x = 0; x < numberOfPoints; x++)
            {
                for (int y = 0; y < numberOfPoints; y++)
                {
                    for (int z = 0; z < numberOfPoints; z++)
                    {
                        Instantiate(pointTemplate, new Vector3(x * res, y * res, z * res), Quaternion.identity);
                    }
                }
            }
        }
    }

    void MarchTheCubes()
    {
        float xoff = 0;
        for (int x = 0; x < numberOfPoints; x++)
        {
            xoff += xInc;
            float yoff = 0;
            for (int y = 0; y < numberOfPoints; y++)
            {
                yoff += yInc;
                float zoff = 0;
                for (int z = 0; z < numberOfPoints; z++)
                {
                    float val = noise.snoise(new float4(xoff, yoff, zoff, woff));
                    points[x, y, z] = val > 0.5f ? 1 : 0;
                    zoff += zInc;
                }
            }
        }

        woff += wInc;
        
        for (int x = 0; x < numberOfPoints - 1; x++)
        {
            for (int y = 0; y < numberOfPoints - 1; y++)
            {
                for (int z = 0; z < numberOfPoints - 1; z++)
                {
                    int[] cube = new int[8];
                    cube[0] = points[x, y, z+1];
                    cube[1] = points[x+1, y, z+1];
                    cube[2] = points[x+1, y, z];
                    cube[3] = points[x, y, z];
                    cube[4] = points[x, y+1, z+1];
                    cube[5] = points[x+1, y+1, z+1];
                    cube[6] = points[x+1, y+1, z];
                    cube[7] = points[x, y+1, z];
                    int currentCase = GetCase(cube);
                    DrawEdges(x * res, y * res, z * res, currentCase);
                }
            }
        }
    }

    void DrawEdges(float x, float y, float z, int currentCase)
    {
        float half = res / 2.0f;
        Vector3 edge0 = new Vector3(x+half, y, z + res);
        Vector3 edge1 = new Vector3(x+res, y, z + half);
        Vector3 edge2 = new Vector3(x+half, y, z);
        Vector3 edge3 = new Vector3(x, y, z + half);
        
        Vector3 edge4 = new Vector3(x+half, y+res, z + res);
        Vector3 edge5 = new Vector3(x+res, y+res, z + half);
        Vector3 edge6 = new Vector3(x+half, y+res, z);
        Vector3 edge7 = new Vector3(x, y+res, z + half);
        
        Vector3 edge8 = new Vector3(x, y+half, z + res);
        Vector3 edge9 = new Vector3(x+res, y+half, z + res);
        Vector3 edge10 = new Vector3(x+res, y+half, z);
        Vector3 edge11 = new Vector3(x, y+half, z);
        
        Vector3[] edgeList = {edge0, edge1, edge2, edge3, edge4, edge5, edge6, edge7, edge8, edge9, edge10, edge11};
        
        for (int i = 0; i < 16; i++)
        {
            if(triangulation[currentCase, i] == -1)
                break;
            
            Vector3 a, b, c;
            a = edgeList[triangulation[currentCase, i]];
            b = edgeList[triangulation[currentCase, i + 1]];
            c = edgeList[triangulation[currentCase, i + 2]];

            i += 2;
            //Mesh triangleMesh = CreateMesh(a, b, c);
            //DrawSingleTriangle(a, b, c, triangleMesh);
            DrawTriangle(a, b, c);
        }
    }
    
    int GetCase(int[] cube){
        int currentCase = 0;
        for (int i = 0; i < 8; i++)
        {
            if (cube[i] == 1)
            {
                currentCase += (int)Mathf.Pow(2, i);
            }
        }
        return currentCase;
    }
    void DrawTriangle(Vector3 a, Vector3 b, Vector3 c)
    {
        GameObject triangle = new GameObject();
        MeshFilter meshFilter = triangle.AddComponent<MeshFilter>();
        MeshRenderer meshRenderer = triangle.AddComponent<MeshRenderer>();
        Mesh mesh = new Mesh();
        mesh.vertices = new Vector3[] {a, b, c};
        mesh.triangles = new int[] {0, 1, 2};
        meshFilter.mesh = mesh;
        meshRenderer.material = new Material(doubleSided);
        mesh.RecalculateNormals();
        createdTriangles.Add(triangle);
    }

    Mesh CreateMesh(Vector3 pointA, Vector3 pointB, Vector3 pointC)
    {
        Mesh mesh = new Mesh();

        // Define the vertices of the triangle
        Vector3[] vertices = new Vector3[] {pointA, pointB, pointC};

        // Define the triangle's indices
        int[] triangles = new int[] {
            0, 1, 2
        };

        // Assign vertices and triangles to the mesh
        mesh.vertices = vertices;
        mesh.triangles = triangles;

        return mesh;
    }
    
    void DrawSingleTriangle(Vector3 pointA, Vector3 pointB, Vector3 pointC, Mesh triangleMesh) {
        Matrix4x4 matrix = Matrix4x4.TRS(Vector3.zero, Quaternion.identity, Vector3.one);

        // Create an array of matrices for a single instance
        Matrix4x4[] matrices = new Matrix4x4[1] { matrix };

        // Draw the mesh instance
        Graphics.DrawMeshInstanced(triangleMesh, 0, material, matrices);
    }

    
