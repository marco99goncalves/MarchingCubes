        //Application.targetFrameRate = 10;
        InitializePoints();
    }

    void DestroyTriangles()
    {
        foreach (GameObject triangle in createdTriangles)
        {
            Destroy(triangle);
        }
        createdTriangles.Clear();
    }
    
    // Update is called once per frame
    void Update()
    {
        if (runSimulation)
        {
            DestroyTriangles();
            MarchTheCubes();
        }
    }
