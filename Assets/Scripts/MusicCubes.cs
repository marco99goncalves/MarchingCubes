        for (int x = 0; x < gridSize; x++)
        {
            for (int y = 0; y < gridSize; y++)
            {
                for (int z = 0; z < gridSize; z++)
                {
                    Vector3 pos = new Vector3(x, y, z);
                    float baseDistance = (pos - center).magnitude;

                    // Calculate modulation index based on position and time phase
                    int modIndex =
                        (int)(Mathf.Sin(x + timePhase) * Mathf.Cos(y - timePhase) * Mathf.Sin(z + timePhase) *
                              _samples.Length) % _samples.Length;
                    modIndex = Mathf.Abs(modIndex); // Ensure index is positive

                    float sampleValue = _samples[modIndex];

                    // Normalize the sample value based on the maximum found
                    float normalizedSampleValue = (maxSampleValue > 0) ? (sampleValue / maxSampleValue) : 0;

                    // Use modIndex to fetch sample and apply to distance

                    float modulatedDistance = baseDistance * (1 + normalizedSampleValue * 50); // Amplify sample impact

                    scalarField[x, y, z] = modulatedDistance - (gridSize / 4);
                }
            }
        }
    }


