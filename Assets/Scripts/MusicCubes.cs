    Mesh GenerateMesh()
    {
        Mesh mesh = new Mesh();
        var vertices = new List<Vector3>();
        var triangles = new List<int>();

        // March through the scalar field
        for (int x = 0; x < gridSize - 1; x++)
        {
            for (int y = 0; y < gridSize - 1; y++)
            {
                for (int z = 0; z < gridSize - 1; z++)
                {
                    // Compute cube index based on the isolevel and the scalar field values at cube corners
                    int cubeIndex = ComputeCubeIndex(x, y, z, isolevel);

                    // No intersection for this cube
                    if (triangulation[cubeIndex, 0] == -1) continue;

                    // Find the vertices where the surface intersects the cube and interpolate positions
                    Vector3[] edgeVertices = InterpolateEdgeVertices(x, y, z);

                    // Create triangles
                    for (int i = 0; triangulation[cubeIndex, i] != -1; i += 3)
                    {
                        triangles.Add(vertices.Count + 0);
                        triangles.Add(vertices.Count + 1);
                        triangles.Add(vertices.Count + 2);
                        vertices.Add(edgeVertices[triangulation[cubeIndex, i]]);
                        vertices.Add(edgeVertices[triangulation[cubeIndex, i + 1]]);
                        vertices.Add(edgeVertices[triangulation[cubeIndex, i + 2]]);
                    }
                }
            }
        }

        bool IsValidVector3(Vector3 v)
        {
            return !float.IsNaN(v.x) && !float.IsInfinity(v.x) &&
                   !float.IsNaN(v.y) && !float.IsInfinity(v.y) &&
                   !float.IsNaN(v.z) && !float.IsInfinity(v.z);
        }

        // Validate vertices
        foreach (Vector3 vertex in vertices)
        {
            if (!IsValidVector3(vertex))
            {
                Debug.LogError("Invalid vertex found: " + vertex);
                return mesh; // Exit the method to prevent setting the mesh
            }
        }

        // Only set the mesh if all vertices are valid
        mesh.vertices = vertices.ToArray();
        mesh.triangles = triangles.ToArray();
        mesh.RecalculateBounds();
        mesh.RecalculateNormals(); // Important for lighting

        return mesh;
    }


    Vector3Int[] CornerTable = new Vector3Int[]
    {
        new Vector3Int(0, 0, 0),
        new Vector3Int(1, 0, 0),
        new Vector3Int(1, 1, 0),
        new Vector3Int(0, 1, 0),
        new Vector3Int(0, 0, 1),
        new Vector3Int(1, 0, 1),
        new Vector3Int(1, 1, 1),
        new Vector3Int(0, 1, 1)
    };

    int ComputeCubeIndex(int x, int y, int z, float isolevel)
    {
        int cubeIndex = 0;
        for (int i = 0; i < 8; i++)
        {
            Vector3Int cornerPos = new Vector3Int(x, y, z) + CornerTable[i];
            if (scalarField[cornerPos.x, cornerPos.y, cornerPos.z] < isolevel)
                cubeIndex |= (1 << i);
        }

        return cubeIndex;
    }

    Vector3[] InterpolateEdgeVertices(int x, int y, int z)
    {
        Vector3[] edgeVertices = new Vector3[12];
        float isolevel = 0.5f; // Example isolevel, adjust as necessary

        // Helper function to get the scalar field value at a given grid position
        float GetScalarFieldValue(int xi, int yi, int zi)
        {
            // Clamp indices to be within the array bounds
            xi = Mathf.Clamp(xi, 0, gridSize - 1);
            yi = Mathf.Clamp(yi, 0, gridSize - 1);
            zi = Mathf.Clamp(zi, 0, gridSize - 1);

            return scalarField[xi, yi, zi];
        }

        // Define the pairs of cube corners that form each edge
        int[][] edgeCorners = new[] { new int[] { 0, 1 }, new int[] { 1, 2 }, new int[] { 2, 3 }, new int[] { 3, 0 }, new int[] { 4, 5 }, new int[] { 5, 6 }, new int[] { 6, 7 }, new int[] { 7, 4 }, new int[] { 0, 4 }, new int[] { 1, 5 }, new int[] { 2, 6 }, new int[] { 3, 7 } };

        for (int i = 0; i < 12; i++)
        {
            // Get the positions and scalar values for the corners forming this edge
            Vector3 p1 = GetCornerPosition(x, y, z, edgeCorners[i][0]);
            Vector3 p2 = GetCornerPosition(x, y, z, edgeCorners[i][1]);
            float val1 = GetScalarFieldValue(x + (int)p1.x, y + (int)p1.y, z + (int)p1.z);
            float val2 = GetScalarFieldValue(x + (int)p2.x, y + (int)p2.y, z + (int)p2.z);

            // Interpolate the position based on the scalar values and isolevel
            edgeVertices[i] = InterpolateVertex(p1, p2, val1, val2, isolevel);
        }

        return edgeVertices;
    }

    // Placeholder for the GetCornerPosition method
    Vector3 GetCornerPosition(int x, int y, int z, int cornerIndex)
    {
        // Offsets for each corner in a cube
        Vector3[] cornerOffsets = new Vector3[]
        {
            new Vector3(0, 0, 0), // Corner 0
            new Vector3(1, 0, 0), // Corner 1
            new Vector3(1, 1, 0), // Corner 2
            new Vector3(0, 1, 0), // Corner 3
            new Vector3(0, 0, 1), // Corner 4
            new Vector3(1, 0, 1), // Corner 5
            new Vector3(1, 1, 1), // Corner 6
            new Vector3(0, 1, 1) // Corner 7
        };

        // Check if the cornerIndex is valid
        if (cornerIndex < 0 || cornerIndex >= cornerOffsets.Length)
        {
            Debug.LogError("Invalid corner index");
            return Vector3.zero;
        }

        // Calculate the world position of the corner
        Vector3 cornerPosition = new Vector3(x, y, z) + cornerOffsets[cornerIndex];

        return cornerPosition;
    }


// Placeholder for the InterpolateVertex method
    Vector3 InterpolateVertex(Vector3 p1, Vector3 p2, float val1, float val2, float isolevel)
    {
        if (Mathf.Abs(isolevel - val1) < 0.00001)
            return p1;
        if (Mathf.Abs(isolevel - val2) < 0.00001)
            return p2;
        if (Mathf.Abs(val1 - val2) < 0.00001)
            return p1;

        float t = (isolevel - val1) / (val2 - val1);
        return p1 + t * (p2 - p1);
    }
