        Vector3 edge0 = new Vector3(x + half, y, z + res);
        Vector3 edge1 = new Vector3(x + res, y, z + half);
        Vector3 edge2 = new Vector3(x + half, y, z);
        Vector3 edge3 = new Vector3(x, y, z + half);

        Vector3 edge4 = new Vector3(x + half, y + res, z + res);
        Vector3 edge5 = new Vector3(x + res, y + res, z + half);
        Vector3 edge6 = new Vector3(x + half, y + res, z);
        Vector3 edge7 = new Vector3(x, y + res, z + half);

        Vector3 edge8 = new Vector3(x, y + half, z + res);
        Vector3 edge9 = new Vector3(x + res, y + half, z + res);
        Vector3 edge10 = new Vector3(x + res, y + half, z);
        Vector3 edge11 = new Vector3(x, y + half, z);

        Vector3[] edgeList = { edge0, edge1, edge2, edge3, edge4, edge5, edge6, edge7, edge8, edge9, edge10, edge11 };

        for (int i = 0; i < 16; i++)
        {
            if (triangulation[currentCase][i] == -1)
                break;

            vertices.Add(edgeList[triangulation[currentCase][i]]);
            triangles.Add(vertices.Count - 1);
        }
    }

